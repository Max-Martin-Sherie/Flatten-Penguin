using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ToolsGoesBRRRRR : MonoBehaviour
{
    private delegate void heyheyehey();
    private static heyheyehey m_heyyy;
    private float HEHEHEHEYy;
    
    [ContextMenu("heyy?")]
    void heyy()
    {
        m_heyyy += heyyi;
    }

    void heyyi() => HEHEHEHEYy = Random.Range(-.2f, .2f);
    
    
    [ContextMenu("heyy!")]
    void heyyoo()
    {
        transform.position += transform.forward * HEHEHEHEYy;
    }
}
